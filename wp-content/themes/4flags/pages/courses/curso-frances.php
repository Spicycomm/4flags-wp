		<h2 class="title-course-section title-course-french">Francês</h2>
		<div class="section section-course section-course-french">
			<div class="courses course-french">
				<p>
					O curso de francês foi elaborado para proporcionar um aprendizado efetivo da língua e 
					cultura francesas.
				</p>
				<p>
					A duração do curso é de 4 anos, sendo obtidos os seguintes níveis de proﬁciência ao ﬁnal de 
					cada ano:
 				</p>

 				<ol>
					<li>1º Ano => Atinge-se o nível de proﬁciência A1/A2 do CEF (Quadro Comum Europeu)</li>
					<li>2º Ano => Atinge-se o nível de proﬁciência A2/B1 do CEF (Quadro Comum Europeu)</li>
					<li>3º Ano => Atinge-se o nível de proﬁciência B1 do CEF (Quadro Comum Europeu)</li>
					<li>4º Ano => Atinge-se o nível de proﬁciência B2 do CEF (Quadro Comum Europeu)</li>
 				</ol>

 				<p>
 					Tout va bien ! est une nouvelle méthode qui propose une démarche très structurée et solide et 
					qui s'appuie sur une compétence de communication générale.
				</p>

				<p>
					Tout va bien ! propose:
				</p>

				<p>
					Des supports et des situations de communication authentiques ou proches de l'authentique,
					permettant à l'élève de se sensibiliser aux différents registres et de découvrir certains aspects 
					de la culture francophone tout autant que la langue.  
				</p>

				<p>
					De très nombreuses activités visant l'acquisition des quatre compétences de communication
					et l'utilisation de stratégies spéciﬁques. 
				</p>

				<p>
					Un travail sur la grammaire et le vocabulaire associés aux situations et au service de la 
					communication faisant une large place à l'observation et à la réflexion Une invitation régulière
					à l'évaluation, au travail en autonomie et à l'auto-évaluation. 
				</p>

				

				<h4>Tout va bien! 1</h4>
				<ul>
					<li>Niveaux: A1, A2,Débutants</li>
					<li>Public: Adultes-Grands adolescents 16-18 ans</li>
				</ul>
				
				<p>
					<strong>Tout va bien! 1</strong> dont les objectifs respectent scrupuleusement les recommandations du CECR propose également un portfolio inclus dans le livrze de l'élève. L'ouvrage s'organise en 6 unités de 2 leçons chacune qui comprennent les rubriques suivantes:
				</p>

				<ul>
					<li>Situations (2 pages de situations de communication)</li>
					<li>Grammaire (2 pages) </li>
					<li>Lexique / Prononciation (1 page)</li>
					<li>Civilisation française et francophone (1 page)</li>
					<li>Compétences (écouter, parler, lire, écrire) </li>
					<li>Evaluation (1 page)</li>
				</ul>
				
				<p><strong>Tout va bien! 1</strong>est prévu pour un enseignement de 120 à 150 heures de cours. Il vise 
					l'acquisition d'une compétence de communication minimal en français. Les étapes sont clairement déﬁnies. L'apprenant pourra se situer dans son apprentissage et acquérir une autonomie progressive: une récapitulation et un bilan sont à faire aprés chaque unité. Il disposer aussi toutes les deux unités d'une activité spéciﬁque qui leur permettra de faire le point. Le portfolio et le passeport qui accompagnent le livre de l'élève servent à l'apprenant à: prendre conscience de son parcours individuel par compétences; attester de son proﬁl insiste particulièrement sur l'acquisition d'une compétence de communication en français. Elle propose un travail rigoureux et systématique qui porte soit sur une compétence isolée, soit sur plusieurs simultanément. Par ce travail, l'apprenant crée ses propores stratégies de compréhension et d'expression. 
				</p>

				<h4>Tout va bien! 2</h4>

				<ul>
					<li>Niveaux:  A2, B1, Intermédiaire</li>
					<li>Public: Adultes - Grands adolescents 16-18 ans</li>
				</ul>

				<p><strong>Tout va bien! 2</strong> poursuit la progression amorcée au niveau débutant. La méthode propose un travail rigoureux qui porte soit sur une compétence isolée, soit sur plusieurs simultanément. Systématiquement, l'apprenant est conduit à développer ses propres stratégies de réception et de production. A ce niveau, la langue écrite devient de plus en plus importante et les médias (radio et presse) apparaissent comme des supports récurrents.
				</p>

				<p><strong>Tout va bien! 2</strong> dont les objectifs respectent scrupuleusement les recommandations du CECR propose également un portfolio inclus dans le livre. Le protfolio se veut:
				</p>

				<ul>
					<li>un instrument d'auto-évaluation permanente;</li>
					<li>un outil d'apprentissage;</li>
					<li>le témoin des progrès de l'apprenant.</li>
					<li>L'ouvrage s'organise en 6 unités de 2 leçons chacune qui comprennent les rubriques suivantes </li>
					<li>: Situations (2 pages de situations de communication)</li>
					<li>Grammaire (2 pages) (en situation et en découverte guidée, avec exercices)</li>
					<li>Lexique / Prononciation (2 pages) </li>
					<li>Civilisation (2 pages)</li>
					<li>Compétences (écouter, parler, lire, écrire) (4 pages)</li>
					<li>Evaluation (1 page).</li>
				</ul>

				<p>
					L'ensemble pédagogique Tout va bien ! 2est prévu pour un enseignement de 120 à 150 heures 
					de cours. Il vise l'acquisition d'une compétence de communication d'un niveau intermédiaire.
					Les objectifs et contenus répondent en particulier aux besoins langagiers - tant écrits qu'oraux 
					- d'une personne ayant des contacts suivis avec des natifs (par exemple, au cours d'un séjour
					professionnel ou touristique en territoire francophone). L'apprenant pourra se situer dans son 
					apprentissage et acquérir une autonomie progressive: une récapitulation et un bilan sont à faire
					après chaque unité. Il disposera aussi toutes les deux unités d'une activité spéciﬁque qui
					permettra de faire le point.
				</p>

				<h4>Tout va bien! 3</h4>

				<ul>
					<li>Niveaux:  Avancé, B1, Intermédiaire</li>
					<li>Public:  Adultes - Grands adolescents 16-18 ans</li>
				</ul>

				<p><strong>Tout va bien! 3</strong> dont les objectifs respectent scrupuleusement les recommandations du CECR propose également un portfolio inclus dans le livre de l'élève pour atteindre le niveau B2.
				La méthode propose un travail rigoureux et systématique qui porte soit sur une compétence isolée, soit sur plusieurs simultanément. Par ce travail, l'apprenant crée ses propres stratégies de compréhension et d'expression. La diversité des stratégies et des modes d'apprentissage sont largement pris en compte dans la méthode avec des rubriques spéciﬁques au long des activités.
				</p>

				<p><strong>Tout va bien! 3</strong> poursuit systématiquement la progression amorcée dans Tout va bien! 1 et 2. 
				La méthode propose un travail rigoureux et régulier qui porte soit sur une compétence isolée soit sur plusieurs simultanément. La langue écrite acquiert un rôle très important et les médias (radio et presse) sont des supports récurrents. Grâce à ses supports, l'apprenant peut développer ses propres stratégies de compréhension et d'expression.
				Le portfolio inclus comporte trois parties : la biographie personnelle, le diagnostic linguistique, ainsi que les bilans-communication de la méthode. Il sert à: <br>prendre conscience des son parcours individuel parcompétences; attester son proﬁl linguistique.
				</p>

				<h4>Tout va bien! 4 </h4>

				<ul>
					<li>Niveaux:  Avancé, B2, Perfectionnement</li>
					<li>Public:  Adultes - Grands adolescents 16-18 ans</li>
				</ul>

				<p>
					<strong>
						Tout va bien ! 4 est une méthode de français destinée aux grands adolescents et adultes 
						ayant atteint un niveau intermédiaire équivalent au niveau B1 et visant le niveau B2 du Cadre 
						européen commun de référence pour les langues (CECR).
					</strong>
				</p>

				<p>
					Les objectifs de Tout va bien ! 4 respectent scrupuleusement les recommandations du CECR,
					comme en témoigne le Portfolio qui accompagne le livre de l'élève. 
				</p>

				<p>
					Tout va bien ! 4 propose :
				</p>

				<p>
					- Des supports de communication authentiques riches et variés, qui permettront à l'apprenant 
					d'appréhender et de manier la langue dans tous ses registres, tout en élargissant sa 
					connaissance de la culture francophone.
				</p>

				<p>
					- De très nombreuses activités destinées à développer les quatre compétences de communication et à utiliser des stratégies spéciﬁques.
				</p>

				<p>
					- Un travail sur la grammaire et le vocabulaire, qui reprend et approfondit l'étude de la langue liées aux intentions de communication en s'attachant aux nuances ; une étude des registres et divers aspects stylistiques incluant une ouverture sur les langues de spécialités.
				</p>

				<p>
					- Des évaluations et des auto-évaluations régulières reposant sur les critères de l'examen DELF 
					B2, toujours accompagnées d'une reflexion personnelle qui conduit à l'autonomie.
				</p>

				<p>
					- Un portfolio intégré qui comprend notamment des ﬁches de stratégies d'apprentissage pour 
					l'étudiant. Le niveau 4 de Tout va bien ! couvre les contenus du niveau B2 du CECR.
				</p>
